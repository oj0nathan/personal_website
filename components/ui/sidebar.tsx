export const DesktopSidebar = ({ className, children, ...props }: React.ComponentProps<typeof motion.div>) => {
  const { open, setOpen, animate } = useSidebar()
  return (
    <motion.div
      className={cn(
        "h-full px-4 py-4 hidden md:flex md:flex-col bg-[#0D1117] border-r border-white/10 flex-shrink-0 fixed left-0 top-0 z-50 overflow-hidden",
        className,
      )}
      animate={{
        width: animate ? (open ? "300px" : "60px") : "300px",
      }}
      onMouseEnter={() => setOpen(true)}
      onMouseLeave={() => setOpen(false)}
      {...props}
    >
      {children}
    </motion.div>
  )
}

export const MobileSidebar = ({ className, children, ...props }: React.ComponentProps<"div">) => {
  const { open, setOpen } = useSidebar()
  return (
    <>
      <div
        className={cn(
          "h-16 px-4 py-4 flex flex-row md:hidden items-center justify-between bg-[#0D1117] border-b border-white/10 w-full fixed top-0 left-0 z-50",
        )}
        {...props}
      >
        <div className="text-2xl font-bold tracking-tight">
          <span className="text-[#1E40AF]">JH</span> <span className="text-white">Jonathan Ho</span>
        </div>
        <div className="flex justify-end z-20">
          <Menu className="text-white cursor-pointer w-6 h-6" onClick={() => setOpen(!open)} />
        </div>
        <AnimatePresence>
          {open && (
            <motion.div
              initial={{ x: "-100%", opacity: 0 }}
              animate={{ x: 0, opacity: 1 }}
              exit={{ x: "-100%", opacity: 0 }}
              transition={{
                duration: 0.3,
                ease: "easeInOut",
              }}
              className={cn(
                "fixed h-full w-full inset-0 bg-[#0D1117] p-10 z-[100] flex flex-col justify-between",
                className,
              )}
            >
              <div className="absolute right-10 top-10 z-50 text-white cursor-pointer" onClick={() => setOpen(!open)}>
                <X />
              </div>
              {children}
            </motion.div>
          )}
        </AnimatePresence>
      </div>
    </>
  )
}

export const SidebarLink = ({
  link,
  className,
  ...props
}: {
  link: Links
  className?: string
  props?: LinkProps
}) => {
  const { open, animate } = useSidebar()
  return (
    <Link
      href={link.href}
      className={cn(
        "flex items-center gap-2 group/sidebar py-3 px-2 rounded-lg hover:bg-[#1E40AF]/10 transition-all duration-200 overflow-hidden",
        className,
      )}
      {...props}
    >
      <div className="text-white group-hover/sidebar:text-[#1E40AF] transition-colors duration-200 flex-shrink-0">
        {link.icon}
      </div>
      <motion.span
        animate={{
          opacity: animate ? (open ? 1 : 0) : 1,
          width: animate ? (open ? "auto" : "0px") : "auto",
        }}
        transition={{ duration: 0.2, ease: "easeInOut" }}
        className="text-white group-hover/sidebar:text-[#1E40AF] text-sm whitespace-nowrap overflow-hidden"
        style={{ display: animate ? (open ? "block" : "none") : "block" }}
      >
        {link.label}
      </motion.span>
    </Link>
  )
}

